int	ft_toupper(int c)
{
	if ('a' <= c && c <= 'z')
		return (c - 32);
	return (c);
}

void	ft_strupper(char *s)
{	
	if (!s)
		return ;
	while (*s)
		ft_toupper(*s++);
}
